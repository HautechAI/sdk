import { useApi } from '../../api-utils';
import { getAccess } from '../../autogenerated/access/access';

export const useAccessApi = () => {
    const api = getAccess();

    return useApi({
        attach: api.accessControllerAttachAccessV1,
        detach: api.accessControllerDetachAccessV1,
        grant: api.accessControllerGrantAccessV1,
        revoke: api.accessControllerRevokeAccessV1,
        list: api.accessControllerAccessV1,
    });
};
