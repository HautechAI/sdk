import { useApi, wrapApiCallNullable } from '../../api-utils';
import { getWorkflows } from '../../autogenerated/workflows/workflows';

export const useWorkflowsApi = () => {
    const hautechApi = getWorkflows();

    return useApi({
        create: hautechApi.workflowsControllerCreateWorkflowV1,
        get: wrapApiCallNullable(hautechApi.workflowsControllerGetWorkflowV1),
        getByAlias: wrapApiCallNullable(hautechApi.workflowsControllerGetPublicWorkflowByAliasV1),
        list: hautechApi.workflowsControllerListWorkflowsV1,
        update: hautechApi.workflowsControllerUpdateWorkflowV1,
        delete: hautechApi.workflowsControllerDeleteWorkflowV1,
        getSchema: hautechApi.workflowsControllerGetWorkflowSchemaV1,
        run: hautechApi.workflowsControllerRunWorkflowV1,
        listPublic: hautechApi.workflowsControllerListPublicWorkflowsV1,
        getPublic: hautechApi.workflowsControllerGetPublicWorkflowV1,
        runPublic: hautechApi.workflowsControllerRunPublicWorkflowV1,
        shareWithEveryone: hautechApi.workflowsControllerShareWorkflowWithEveryoneV1,
        revokePublicAccess: hautechApi.workflowsControllerRevokeWorkflowPublicAccessV1,
        getStatistics: hautechApi.workflowsControllerGetSharedWorkflowStatisticsV1,
    });
};
