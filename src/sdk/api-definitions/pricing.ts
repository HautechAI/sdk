import { useApi, wrapApiCallNullable } from '../../api-utils';
import { getPricing } from '../../autogenerated/pricing/pricing';

export const usePricingApi = () => {
    const hautechApi = getPricing();

    return useApi({
        list: hautechApi.pricingControllerListPricingV1,
        get: wrapApiCallNullable(hautechApi.pricingControllerGetPricingV1),
    });
};
