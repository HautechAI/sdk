import { useApi, wrapApiCallNullable } from '../../api-utils';
import { getLora } from '../../autogenerated/lora/lora';

export const useLorasApi = () => {
    const hautechApi = getLora();

    return useApi({
        get: wrapApiCallNullable(hautechApi.loraControllerGetLoraByIdV1),
        list: hautechApi.loraControllerGetLorasV1,
    });
};